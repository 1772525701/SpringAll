package com.example.mapper;

import java.util.List;

import org.apache.ibatis.annotations.Mapper;
import org.apache.ibatis.annotations.Param;
import org.apache.ibatis.annotations.Select;

import com.baomidou.mybatisplus.core.mapper.BaseMapper;
import com.baomidou.mybatisplus.core.metadata.IPage;
import com.baomidou.mybatisplus.extension.plugins.pagination.Page;
import com.example.model.LoggerModel;

//BaseMapper<T> 继承该接口后，无需编写 mapper.xml 文件，即可获得CRUD功能
@Mapper
public interface EasyMapper extends BaseMapper<LoggerModel> {

	@Select("select top 10 * from Logger")
	List<LoggerModel> selectAll();
	
	/**
     * <p>
     * 查询 : 根据state状态查询用户列表，分页显示
     * 注意!!: 如果入参是有多个,需要加注解指定参数名才能在xml中取值
     * </p>
     *
     * @param page 分页对象,xml中可以从里面进行取值,传递参数 Page 即自动分页,必须放在第一位(你可以继承Page实现自己的分页对象)
     * @param state 状态
     * @return 分页对象
     */
    IPage<LoggerModel> selectPageVo(Page page, @Param("state") Integer state);

}
